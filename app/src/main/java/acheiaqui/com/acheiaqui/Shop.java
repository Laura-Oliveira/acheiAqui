package acheiaqui.com.acheiaqui;

public class Shop
{
    private String name;
    private String info;

    public Shop(String name, String info)
    {
        this.name = name;
        this.info = info;
    }

    public String getName()
    {
        return this.name;
    }

    public String getInfo() { return this.info; }
}
